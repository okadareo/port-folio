$(function () {
  function readURL(input) {
    if (input.files) {
      var reader = new FileReader();
      reader.onload = function (e) {
        $('.image-background').attr('src', e.target.result);
      }
      reader.readAsDataURL(input.files[0]);
    }
  }

  $('.background-image').change(function () {
    readURL(this);
  });
});


